export enum HostDefaults {
	/**
	 * The configuration key used to set {@link IHostEnv.appName}.
	 */
	AppKey = 'applicationName',
	/**
	 * The configuration key used to set {@link IHostEnv.envName}.
	 */
	EnvKey = 'environment',
	/**
	 * The configuration key used to set {@link IHostEnv.contentRootPath}
	 * and {@link IHostEnv.contentRootFileProvider}.
	 */
	ContentRootKey = 'contentRoot',
}
