// An enumeration for the xml:space scope used in XmlReader and XmlWriter.
export enum XmlSpace {
	// xml:space scope has not been specified.
	None = 0,
	// The xml:space scope is "default".
	Default = 1,
	// The xml:space scope is "preserve".
	Preserve = 2,
}
